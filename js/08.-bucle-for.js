'use strict'

// Bucle For
// Es una estructura de control que se repite varias veces 

let numero=100;

// let i=1 -> contador
// i <= numero -> condicion
// i++ -> incrementador

for(let i = 1; i <= numero; i++){
    console.log("Numero: " + i);

    // debugger; // Permite ver el flujo del codigo hasta que termine el bucle.
}